
verdade = 'PYthOn, é iNCrível' 
# lower() -> Retornar uma cópia da string em letras minusculas
print(verdade.lower())
# upper() -> Retornar uma cópia da string em letras maiusculas
print(verdade.upper())
# replace() -> substituir uma palavrapor outra
# repalce(antiga, nova)
print(verdade.replace('PYthOn', 'SQL'))
# split() -> divide a string a partir do delimitador
print(verdade.split(','))
print(verdade.capitalize())
print(verdade)
